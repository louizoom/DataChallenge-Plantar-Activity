    "def compare_total_force_by_action(df, class1=\"Standing\", class2=\"Walking\"):\n",
    "    \"\"\"\n",
    "    Génère un boxplot et/ou barplot quantitatif de la \"Total Force\" sur deux ou plusieurs actions comparées.\n",
    "    \"\"\"\n",
    "    if df is None: return\n",
    "    \n",
    "    df_comp = df[df['Action_Name'].isin([class1, class2])].copy()\n",
    "    force_cols = [c for c in df.columns if 'total' in c.lower() and 'force' in c.lower()]\n",
    "    \n",
    "    if df_comp.empty or not force_cols:\n",
    "        print(f\"⚠️ Soit les classes n'existent pas, soit la colonne Total Force manque.\")\n",
    "        return\n",
    "        \n",
    "    # Scénario A : Nous avons deux colonnes (Force Totale Gauche, Force Totale Droite)\n",
    "    if len(force_cols) >= 2:\n",
    "        fL, fR = force_cols[0], force_cols[1]\n",
    "        # Transformation en tableau long (Melt) pour Seaborn Hue\n",
    "        df_melt = pd.melt(df_comp, id_vars=['Action_Name'], value_vars=[fL, fR], \n",
    "                          var_name='Côté / Source', value_name='Valeur Force Totale')\n",
    "        \n",
    "        plt.figure(figsize=(9, 6))\n",
    "        sns.barplot(data=df_melt, x='Action_Name', y='Valeur Force Totale', hue='Côté / Source', palette='Set1', capsize=0.1)\n",
    "        plt.title(f'Disparité de Force Totale Bilatérale : {class1} vs {class2}', fontweight='bold')\n",
    "        plt.xlabel('Action ciblée')\n",
    "        plt.ylabel('Moyenne de la Force Totale')\n",
    "        plt.tight_layout()\n",
    "        plt.show()\n",
    "        \n",
    "    # Scénario B : Une seule colonne agglomérée\n",
    "    else:\n",
    "        fc = force_cols[0]\n",
    "        plt.figure(figsize=(8, 6))\n",
    "        sns.boxplot(data=df_comp, x='Action_Name', y=fc, palette='pastel')\n",
    "        plt.title(f'Disparité de Force Totale Globale : {class1} vs {class2}', fontweight='bold')\n",
    "        plt.xlabel('Action ciblée')\n",
    "        plt.ylabel(fc)\n",
    "        plt.tight_layout()\n",
    "        plt.show()\n",
