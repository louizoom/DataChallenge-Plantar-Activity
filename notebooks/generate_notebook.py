    "def visualize_signals_window(df, target_action=\"Walking\"):\n",
    "    \"\"\"\n",
    "    Restreint les données à l'action visée, et trace la moyenne des pressions, les\n",
    "    trois axes de l'IMU et le chemin du Centre de Pression (CoP) sur un bloc temporel.\n",
    "    \"\"\"\n",
    "    if df is None: return\n",
    "    \n",
    "    df_action = df[df['Action_Name'] == target_action].copy()\n",
    "    if df_action.empty:\n",
    "        print(f\"⚠️ Aucune donnée pour l'action '{target_action}'.\")\n",
    "        return\n",
    "        \n",
    "    # Extraction de la PREMIÈRE séquence ininterrompue\n",
    "    # Si l'écart de temps dépasse 2 frames (0.02s à 100fps), on considère une coupure.\n",
    "    blocks = (df_action['Time'].diff() > 0.02).cumsum()\n",
    "    df_window = df_action[blocks == blocks.iloc[0]]\n",
    "    time_arr = df_window['Time']\n",
    "    \n",
    "    print(f\"🎬 Étude temporelle [{target_action}] de {time_arr.min():.2f}s à {time_arr.max():.2f}s\")\n",
    "    \n",
    "    # ==================\n",
    "    # 3.A. PRESSIONS MOYENNES (G vs D)\n",
    "    # ==================\n",
    "    press_L = [c for c in df_window.columns if ('press' in c.lower() or 'pr_' in c.lower()) and ('_l' in c.lower() or 'left' in c.lower())][:16]\n",
    "    press_R = [c for c in df_window.columns if ('press' in c.lower() or 'pr_' in c.lower()) and ('_r' in c.lower() or 'right' in c.lower())][:16]\n",
    "    \n",
    "    if press_L and press_R:\n",
    "        mean_L = df_window[press_L].mean(axis=1)\n",
    "        mean_R = df_window[press_R].mean(axis=1)\n",
    "        \n",
    "        plt.figure(figsize=(12, 4))\n",
    "        plt.plot(time_arr, mean_L, label='Gauches Moyennes', color='royalblue', lw=2)\n",
    "        plt.plot(time_arr, mean_R, label='Droites Moyennes', color='firebrick', lw=2)\n",
    "        plt.title(f'Activation Plantaire Moyenne par pied - {target_action}', fontweight='bold')\n",
    "        plt.xlabel('Temps chronologique (s)')\n",
    "        plt.ylabel('Pression moyenne')\n",
    "        plt.legend()\n",
    "        plt.tight_layout()\n",
    "        plt.show()\n",
    "        \n",
    "    # ==================\n",
    "    # 3.B. IMU (Accélération Pied Droit)\n",
    "    # ==================\n",
    "    acc_R = [c for c in df_window.columns if 'acc' in c.lower() and ('_r' in c.lower() or 'right' in c.lower())][:3]\n",
    "    if len(acc_R) == 3:\n",
    "        ax, ay, az = acc_R\n",
    "        plt.figure(figsize=(12, 4))\n",
    "        plt.plot(time_arr, df_window[ax], label=f'Axe X ({ax})', alpha=0.8)\n",
    "        plt.plot(time_arr, df_window[ay], label=f'Axe Y ({ay})', alpha=0.8)\n",
    "        plt.plot(time_arr, df_window[az], label=f'Axe Z ({az})', alpha=0.8)\n",
    "        plt.title(f'Données inertielles (Accélération Pied Droit) - {target_action}', fontweight='bold')\n",
    "        plt.xlabel('Temps chronologique (s)')\n",
    "        plt.ylabel('Accélération transmise')\n",
    "        plt.legend(loc=\"upper right\")\n",
    "        plt.tight_layout()\n",
    "        plt.show()\n",
    "        \n",
    "    # ==================\n",
    "    # 3.C. CENTRE DE PRESSION (CoP Pied Droit)\n",
    "    # ==================\n",
    "    cop_R = [c for c in df_window.columns if 'cop' in c.lower() and ('_r' in c.lower() or 'right' in c.lower())][:2]\n",
    "    if len(cop_R) == 2:\n",
    "        cx, cy = cop_R\n",
    "        plt.figure(figsize=(7, 6))\n",
    "        # Représentation de l'écoulement temporel par la couleur\n",
    "        scatter = plt.scatter(df_window[cx], df_window[cy], c=time_arr, cmap='plasma', s=45)\n",
    "        # Ligne d'historique\n",
    "        plt.plot(df_window[cx], df_window[cy], c='grey', linestyle='-', lw=1, alpha=0.4)\n",
    "        \n",
    "        plt.title(f'Trajectoire 2D du Centre de Pression (Pied Droit) - {target_action}', fontweight='bold')\n",
    "        plt.xlabel(cx)\n",
    "        plt.ylabel(cy)\n",
    "        cbar = plt.colorbar(scatter)\n",
    "        cbar.set_label('Temps parcouru (s)')\n",
    "        plt.grid(True)\n",
    "        plt.tight_layout()\n",
    "        plt.show()\n",
    "\n",
    "if df_merged is not None:\n",
    "    visualize_signals_window(df_merged, target_action=\"Walking\")"
