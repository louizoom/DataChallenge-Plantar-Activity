    "def load_and_merge_data(subject_id='01', sequence='Sequence_01'):\n",
    "    \"\"\"\n",
    "    Charge et fusionne les données de capteurs (insoles.csv) et \n",
    "    les annotations (classif.csv) d'un sujet et d'une séquence spécifique.\n",
    "    Fréquence : 100 fps. Séparateur : ';'\n",
    "    \"\"\"\n",
    "    insoles_path = os.path.join(PLANTAR_DIR, f\"S{subject_id}\", sequence, \"insoles.csv\")\n",
    "    classif_path = os.path.join(EVENTS_DIR, f\"S{subject_id}\", sequence, \"classif.csv\")\n",
    "    \n",
    "    if not os.path.exists(insoles_path):\n",
    "        print(f\"⚠️ Le fichier {insoles_path} est introuvable.\")\n",
    "        return None\n",
    "        \n",
    "    if not os.path.exists(classif_path):\n",
    "        print(f\"⚠️ Le fichier {classif_path} est introuvable.\")\n",
    "        return None\n",
    "        \n",
    "    print(f\"✅ Chargement des données pour le sujet S{subject_id} - {sequence}\")\n",
    "    \n",
    "    # Chargement (avec séparateur point-virgule comme spécifié)\n",
    "    df_insoles = pd.read_csv(insoles_path, sep=';')\n",
    "    df_classif = pd.read_csv(classif_path, sep=';')\n",
    "    \n",
    "    # Création des colonnes cibles pour stocker les annotations\n",
    "    df_insoles['Class'] = np.nan\n",
    "    df_insoles['Action_Name'] = 'Transition/Unknown'\n",
    "    \n",
    "    # Alignement temporel : pour chaque événement, on l'associe à la plage de temps correspondante\n",
    "    for _, row in df_classif.iterrows():\n",
    "        t_start = row['Timestamp Start']\n",
    "        t_end = row['Timestamp End']\n",
    "        c_id = row['Class']\n",
    "        c_name = row['Name']\n",
    "        \n",
    "        # Création d'un masque pour encadrer chronologiquement l'action\n",
    "        mask = (df_insoles['Time'] >= t_start) & (df_insoles['Time'] <= t_end)\n",
    "        \n",
    "        # Affectation\n",
    "        df_insoles.loc[mask, 'Class'] = c_id\n",
    "        df_insoles.loc[mask, 'Action_Name'] = c_name\n",
    "        \n",
    "    return df_insoles\n",
